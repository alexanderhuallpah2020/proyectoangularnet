//    {
//        opcionesCORS.AllowAnyOrigin().AllowAnyMethod().AllowAnyHeader();
//    });
//});



var app = builder.Build();

// Configure the HTTP request pipeline.
if (app.Environment.IsDevelopment())
{
    app.UseSwagger();
    app.UseSwaggerUI();
}

app.UseHttpsRedirection();


app.UseOutputCache();

app.UseCors();

app.UseAuthorization();

app.MapControllers();

app.Run();
